type Profile struct {
	Name            string // 姓名
	Age             int    // 年龄
	Gender          string // 性别
	Height          int    // 身高
	Weight          int    // 体重
	WorkingLocation string // 工作地
	Income          string // 收入
	Marriage        string // 婚况
	Education       string // 教育
	Occupation      string // 职业
	Hometown        string // 籍贯
	Nation          string // 民族
	Constellation   string // 星座
	House           string // 是否购房
	Car             string // 是否购车
	Drinking        string // 是否喝酒
	Smoking         string // 是否抽烟
}
